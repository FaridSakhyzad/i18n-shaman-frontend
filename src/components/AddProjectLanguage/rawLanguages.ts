const getFilteredRawLangs = () => {
  const rawLangs = getRawLangs();

  const regex = /\([0-9A-Za-z\s_-]*\)$/ig;

  let mapped = rawLangs.map((lang) => {
    if (lang.label.includes('custom')) {
      return null
    }

    if (!lang.label || !lang.label.match(regex)) {
      return {
        xUnmatchedLanguage: lang
      };
    }

    // @ts-ignore
    let code = lang.label.match(regex)[0];

    const label = lang.label.replace(code, '');

    code = code.replace('(', '').replace(')', '');

    return {
      code,
      label: label.trim(),
      originalLabel: lang.label,
      originalValue: lang.value,
      xUnmatchedLanguage: false,
    };
  });

  mapped = mapped.filter(Boolean);

  const unmatchedLanguages = mapped.filter(lang => {
    return lang && lang.xUnmatchedLanguage
  })
}

